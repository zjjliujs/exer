var fruit = ['apples', 'bananas', 'Cherries'];
console.log (fruit);
fruit.sort(); // ['Cherries', 'apples', 'bananas'];
console.log (fruit);

var scores = [1, 2, 10, 21];
console.log (scores);
scores.sort(); // [1, 10, 2, 21]
console.log (scores);

var things = ['word', 'Word', '1 Word', '2 Words'];
console.log (things);
things.sort(); // ['1 Word', '2 Words', 'Word', 'word']
console.log (things);

